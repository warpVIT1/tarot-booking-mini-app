    getSupportedLanguages() {
        return this.supportedLanguages.map(lang => ({
            code: lang,
            name: this.getLanguageName(lang),
            nativeName: this.getLanguageNativeName(lang),
            flag: this.getLanguageFlag(lang)
        }));
    },

