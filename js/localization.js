    applyLanguage() {
        try {
            // –û–±–Ω–æ–≤–ª—è–µ–º title —Å—Ç—Ä–∞–Ω–∏—Ü—ã
            document.title = this.t('app_name');

            // –û–±–Ω–æ–≤–ª—è–µ–º –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã —Å data-i18n
            document.querySelectorAll('[data-i18n]').forEach(element => {
                const key = element.getAttribute('data-i18n');
                const translation = this.t(key);

                if (element.tagName === 'INPUT' && (element.type === 'text' || element.type === 'email')) {
                    element.placeholder = translation;
                } else {
                    element.textContent = translation;
                }
            });

            // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –∞—Ç—Ä–∏–±—É—Ç lang –¥–ª—è HTML
            document.documentElement.lang = this.currentLanguage;

            // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ —Ç–µ–∫—Å—Ç–∞ (–¥–ª—è –±—É–¥—É—â–µ–≥–æ —Ä–∞—Å—à–∏—Ä–µ–Ω–∏—è)
            document.documentElement.dir = this.isRTL() ? 'rtl' : 'ltr';

            ConfigUtils.log('üåê –Ø–∑—ã–∫ –ø—Ä–∏–º–µ–Ω–µ–Ω –∫ –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å—É');

        } catch (error) {
            ConfigUtils.error('–û—à–∏–±–∫–∞ –ø—Ä–∏–º–µ–Ω–µ–Ω–∏—è —è–∑—ã–∫–∞:', error);
        }
    },

    // –ü–æ–ª—É—á–∏—Ç—å –ø–µ—Ä–µ–≤–æ–¥ –ø–æ –∫–ª—é—á—É
    t(key, params = {}) {
        try {
            const translation = this.translations[this.currentLanguage]?.[key] ||
                               this.translations['ru'][key] ||
                               key;

            // –ü–æ–¥—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤
            return translation.replace(/\{(\w+)\}/g, (match, param) => {
                return params[param] || match;
            });

        } catch (error) {
            ConfigUtils.error('–û—à–∏–±–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è –ø–µ—Ä–µ–≤–æ–¥–∞ –¥–ª—è –∫–ª—é—á–∞:', key, error);
            return key;
        }
    },

    // –°–º–µ–Ω–∏—Ç—å —è–∑—ã–∫
    setLanguage(language) {
        if (!this.supportedLanguages.includes(language)) {
            ConfigUtils.error('–ù–µ–ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º—ã–π —è–∑—ã–∫:', language);
            return false;
        }

        this.currentLanguage = language;

        // –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏
        const settings = ConfigUtils.getUserSettings();
        settings.language = language;
        ConfigUtils.saveUserSettings(settings);

        // –ü—Ä–∏–º–µ–Ω—è–µ–º –∫ –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å—É
        this.applyLanguage();

        // –û–±–Ω–æ–≤–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤ —Å–∏—Å—Ç–µ–º–µ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏
        if (typeof AuthSystem !== 'undefined' && AuthSystem.getCurrentUser()) {
            AuthSystem.updateUser({
                language: language,
                languageCode: language
            });
        }

        ConfigUtils.log('üåê –Ø–∑—ã–∫ –∏–∑–º–µ–Ω–µ–Ω –Ω–∞:', language);

        // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ
        if (typeof Components !== 'undefined') {
            Components.showNotification(this.t('language_changed'), 'success');
        }

        return true;
    },

    // –ü–æ–ª—É—á–∏—Ç—å —Ç–µ–∫—É—â–∏–π —è–∑—ã–∫
    getCurrentLanguage() {
        return this.currentLanguage;
    },

