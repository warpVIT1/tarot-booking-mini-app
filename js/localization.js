                detectedLanguage = savedSettings.language;
                ConfigUtils.log('üåê –Ø–∑—ã–∫ –∏–∑ –Ω–∞—Å—Ç—Ä–æ–µ–∫:', detectedLanguage);
                this.currentLanguage = detectedLanguage;
                return;
