    formatDate(date, options = {}) {
        const locale = this.getLocale();
        return new Date(date).toLocaleDateString(locale, options);
    },

    // –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –≤—Ä–µ–º–µ–Ω–∏ –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å –ª–æ–∫–∞–ª—å—é
    formatTime(date, options = {}) {
        const locale = this.getLocale();
        return new Date(date).toLocaleTimeString(locale, options);
    },

    // –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –¥–∞—Ç—ã –∏ –≤—Ä–µ–º–µ–Ω–∏
    formatDateTime(date, options = {}) {
        const locale = this.getLocale();
        return new Date(date).toLocaleString(locale, options);
    },

    // –ü–æ–ª—É—á–∏—Ç—å –ª–æ–∫–∞–ª—å –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
    getLocale() {
        const locales = {
            'ru': 'ru-RU',
            'en': 'en-US',
            'uk': 'uk-UA'
        };
        return locales[this.currentLanguage] || 'ru-RU';
    },

    // –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —á–∏—Å–µ–ª
    formatNumber(number, options = {}) {
        const locale = this.getLocale();
        return new Intl.NumberFormat(locale, options).format(number);
    },

    // –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –≤–∞–ª—é—Ç—ã
    formatCurrency(amount, currency = 'UAH') {
        const locale = this.getLocale();

        // –û–ø—Ä–µ–¥–µ–ª—è–µ–º –≤–∞–ª—é—Ç—É –ø–æ —è–∑—ã–∫—É –µ—Å–ª–∏ –Ω–µ —É–∫–∞–∑–∞–Ω–∞
        if (currency === 'UAH') {
            const currencies = {
                'ru': 'RUB',
                'en': 'USD',
                'uk': 'UAH'
            };
            currency = currencies[this.currentLanguage] || 'UAH';
        }

        return new Intl.NumberFormat(locale, {
            style: 'currency',
            currency: currency
        }).format(amount);
    },

